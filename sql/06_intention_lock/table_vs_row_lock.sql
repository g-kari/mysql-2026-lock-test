-- =====================================================
-- Intention Lock（インテンションロック）- テーブルロック vs 行ロックの競合
-- 目的: IS/IXインテンションロックがテーブルロックとどのように競合するかを確認
--       行ロック取得時に自動的にテーブルにIS/IXが設定されることを観察
-- =====================================================
USE lock_test_db;

-- インテンションロック互換性マトリクス（テーブルレベル）:
--
--            要求ロック
--           | IS  | IX  | S   | X
-- 保持ロック |-----|-----|-----|----
-- IS         |  ○ |  ○ |  ○ |  ×
-- IX         |  ○ |  ○ |  ×  |  ×
-- S          |  ○ |  ×  |  ○ |  ×
-- X          |  ×  |  ×  |  ×  |  ×
--
-- IS: Intent Shared  (FOR SHARE 時にテーブルに設定)
-- IX: Intent Exclusive (FOR UPDATE 時にテーブルに設定)

SELECT id, name, balance FROM accounts ORDER BY id;

-- =====================================================
-- Step 1: Session A - FOR UPDATE（テーブルにIXが設定される）
-- 別ターミナル(Session A)で実行
-- =====================================================
-- BEGIN;
-- SELECT * FROM accounts WHERE id = 30 FOR UPDATE;
-- -- 自動的にテーブルに IX が設定される

-- =====================================================
-- Step 2: Observer - テーブルレベルのIXロック確認
-- 別ターミナル(Observer)で実行
-- =====================================================
-- source sql/helpers/observe_locks.sql
--
-- 期待される出力:
--   LOCK_TYPE | LOCK_MODE     | OBJECT_NAME | INDEX_NAME
--   TABLE     | IX            | accounts    | NULL       ← テーブルIX
--   RECORD    | X,REC_NOT_GAP | accounts    | PRIMARY    ← 行ロック
--
-- IXロックの役割:
--   → "このテーブルの特定の行に排他ロックを保持している" というシグナル
--   → テーブル全体の排他ロック（LOCK TABLES WRITE）が取得されるとき
--     IX と競合するかチェックできる（全行スキャン不要）

-- =====================================================
-- Step 3: Session B - テーブルロック試行（IXと競合）
-- 別ターミナル(Session B)で実行
-- =====================================================
-- -- IXを保持中にテーブル排他ロックはブロック
-- LOCK TABLES accounts WRITE;
-- -- ← ブロック！（IX と テーブルX が競合）
--
-- テーブル共有ロックも同様にブロック:
-- LOCK TABLES accounts READ;
-- -- ← ブロック！（IX と テーブルS が競合）

-- =====================================================
-- Step 4: Observer - 待機状態確認
-- 別ターミナル(Observer)で実行
-- =====================================================
-- source sql/helpers/observe_lock_waits.sql

-- =====================================================
-- Step 5: Session A - コミット（Session Bのブロック解除）
-- 別ターミナル(Session A)で実行
-- =====================================================
-- COMMIT;
-- -- Session BのLOCK TABLES が完了する

-- =====================================================
-- クリーンアップ
-- =====================================================
-- Session B: UNLOCK TABLES;
-- Observer:  source sql/helpers/reset_data.sql

-- =====================================================
-- 調査結果メモ
-- =====================================================
-- 実測後にここに記入:
-- - FOR UPDATE時のテーブルLOCK_MODE: IX
-- - FOR SHARE時のテーブルLOCK_MODE: IS
-- - IX保持中のLOCK TABLES WRITE: ブロックあり
-- - IX保持中のLOCK TABLES READ: ブロックあり
-- - IX同士（別行）: ブロックなし（IXとIXは互換）
-- - 備考: インテンションロックの存在によりテーブル全体スキャン不要
