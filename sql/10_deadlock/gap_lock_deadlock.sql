-- ギャップロックによるデッドロック
-- 目的: ギャップロック同士の循環による微妙なデッドロックを実証
--       Gap Lockは互いに互換だが、その後のINSERTがデッドロックを引き起こす
-- =====================================================
USE lock_test_db;

-- accounts PK: 10, 20, 30, 40, 50
SELECT id, name, balance FROM accounts ORDER BY id;

-- =====================================================
-- ギャップロックのデッドロックシナリオ
-- =====================================================
-- 1. Session A: 範囲(20,40)にギャップロック取得
-- 2. Session B: 範囲(25,45)にギャップロック取得
--    （Gap Lock同士は互換 → どちらも GRANTED）
-- 3. Session A: ギャップ内にINSERT（id=35）→ Session Bのギャップロックで待機
-- 4. Session B: ギャップ内にINSERT（id=27）→ Session Aのギャップロックで待機
--    → 循環待機 → デッドロック！

-- =====================================================
-- Step 1: Session A - ギャップロック取得
-- 別ターミナル(Session A)で実行
-- =====================================================
-- SET TRANSACTION ISOLATION LEVEL REPEATABLE READ;
-- BEGIN;
-- SELECT * FROM accounts WHERE id > 20 AND id < 40 FOR UPDATE;
-- -- Gap Lock: (20,30), (30,40) を取得（GRANTED）

-- =====================================================
-- Step 2: Session B - 重複するギャップをロック（互換なので即時取得）
-- 別ターミナル(Session B)で実行
-- =====================================================
-- SET TRANSACTION ISOLATION LEVEL REPEATABLE READ;
-- BEGIN;
-- SELECT * FROM accounts WHERE id > 25 AND id < 45 FOR UPDATE;
-- -- Gap Lock: (20,30), (30,40), (40,50) を取得（GRANTED）
-- -- Session AとBの Gap Lock は互換のため両方 GRANTED
-- -- ← 即時完了（デッドロックはまだ発生しない）

-- =====================================================
-- Step 3: Observer - 両セッションのギャップロックが GRANTED であることを確認
-- 別ターミナル(Observer)で実行
-- =====================================================
-- source sql/helpers/observe_locks.sql
-- -- Session A: X,GAP on 30, 40（GRANTED）
-- -- Session B: X,GAP on 30, 40, 50（GRANTED）

-- =====================================================
-- Step 4: Session A - ギャップ内にINSERT（Session Bのギャップロックで待機）
-- 別ターミナル(Session A)で実行
-- =====================================================
-- INSERT INTO accounts (id, name, balance) VALUES (35, 'FromA', 100.00);
-- -- ← Session Bがギャップ(30,40)をロック中 → INSERT Intentionが競合 → 待機

-- =====================================================
-- Step 5: Session B - ギャップ内にINSERT（デッドロック発生！）
-- 別ターミナル(Session B)で実行
-- =====================================================
-- INSERT INTO accounts (id, name, balance) VALUES (27, 'FromB', 200.00);
-- -- ← Session Aがギャップ(20,30)をロック中 → INSERT Intentionが競合
-- -- 循環待機: A → B のギャップ待機、B → A のギャップ待機
