-- =====================================================
-- テーブル間デッドロック（逆順ロック）
-- 目的: 2つのテーブルに対して逆順でロックを取得した場合のデッドロック
--       外部キーがある場合の暗黙的ロックについても説明する
-- =====================================================
USE lock_test_db;

SELECT id, name, balance FROM accounts ORDER BY id;
SELECT id, account_id, amount, status FROM orders ORDER BY id;

-- =====================================================
-- シナリオ: accounts と orders を逆順でロック
-- =====================================================
-- Session A: accounts(id=10) → orders(id=1) の順でロック
-- Session B: orders(id=1)   → accounts(id=10) の順でロック
-- → 循環待機 → デッドロック

-- =====================================================
-- Step 1: Session A - accounts(id=10) をロック
-- 別ターミナル(Session A)で実行
-- =====================================================
-- BEGIN;
-- SELECT * FROM accounts WHERE id = 10 FOR UPDATE;
-- -- accounts の id=10 に X,REC_NOT_GAP

-- =====================================================
-- Step 2: Session B - orders(id=1) をロック
-- 別ターミナル(Session B)で実行
-- =====================================================
-- BEGIN;
-- SELECT * FROM orders WHERE id = 1 FOR UPDATE;
-- -- orders の id=1 に X,REC_NOT_GAP（account_id=10の注文）

-- =====================================================
-- Step 3: Observer - 現在のロック状態
-- 別ターミナル(Observer)で実行
-- =====================================================
-- source sql/helpers/observe_locks.sql
-- -- accounts: Session A が id=10 保持
-- -- orders:   Session B が id=1 保持

-- =====================================================
-- Step 4: Session A - orders(id=1) をロック試行（待機）
-- 別ターミナル(Session A)で実行
-- =====================================================
-- SELECT * FROM orders WHERE id = 1 FOR UPDATE;
-- -- ← Session B が id=1 を保持しているため待機

-- =====================================================
-- Step 5: Session B - accounts(id=10) をロック試行（デッドロック！）
-- 別ターミナル(Session B)で実行
-- =====================================================
-- SELECT * FROM accounts WHERE id = 10 FOR UPDATE;
-- -- ← Session A が id=10 を保持 + Session AはSession Bのorders待ち
-- -- → 循環: A→orders待機、B→accounts待機
-- -- ERROR 1213 (40001): Deadlock found

-- =====================================================
-- Step 6: Observer - デッドロック確認
-- 別ターミナル(Observer)で実行
-- =====================================================
-- source sql/helpers/show_engine_status.sql

-- =====================================================
-- 外部キー制約がある場合の暗黙的ロック（補足）
-- =====================================================
-- 外部キー制約（例: orders.account_id → accounts.id）がある場合:
--
-- ordersへのINSERT時:
--   → accounts の参照先行に S ロック（共有ロック）が暗黙的に設定される
--   → INSERT INTO orders ... WHERE account_id=10
--     → accounts の id=10 に S,REC_NOT_GAP が自動設定
--
-- ordersのaccount_idをUPDATE時:
--   → 旧account_idの親行にS、新account_idの親行にSが設定される
--
-- accountsのidをUPDATE/DELETE時:
--   → 子テーブル(orders)の対応行に暗黙的なSまたはXロック
--
-- このため外部キー制約が存在する場合は:
--   「親テーブル操作 ↔ 子テーブル操作」の逆順でデッドロックが発生しやすい

-- =====================================================
-- デッドロック回避策
-- =====================================================
-- 1. 全トランザクションでテーブルロック順序を統一
--    例: 常に accounts → orders の順でロック
-- 2. 外部キーがある場合は参照方向を意識
-- 3. SELECT ... FOR UPDATE の前に全ての必要なロックを取得
-- 4. アプリケーション側でデッドロック(1213)をキャッチしリトライ

