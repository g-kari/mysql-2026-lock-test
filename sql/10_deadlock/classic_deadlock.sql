-- =====================================================
-- 古典的デッドロック
-- 目的: 最もシンプルなデッドロックパターンを実証
--       Session A: Row10→Row20の順でロック
--       Session B: Row20→Row10の順でロック（逆順）
--       → 循環待機 → InnoDBが自動検知してロールバック
-- =====================================================
USE lock_test_db;

SELECT id, name, balance FROM accounts ORDER BY id;

-- =====================================================
-- デッドロックのメカニズム
-- =====================================================
-- 1. Session A: id=10 をロック（保持）
-- 2. Session B: id=20 をロック（保持）
-- 3. Session A: id=20 をロックしようとする → Session Bが保持中 → 待機
-- 4. Session B: id=10 をロックしようとする → Session Aが保持中 → 待機
-- 5. 循環待機発生 → InnoDBがwait-forグラフを検知 → デッドロック判定
-- 6. InnoDB: コスト（修正行数など）が小さい方のトランザクションを自動ロールバック

-- =====================================================
-- Step 1: Session A - id=10 をロック
-- 別ターミナル(Session A)で実行
-- =====================================================
-- BEGIN;
-- SELECT * FROM accounts WHERE id = 10 FOR UPDATE;
-- -- id=10 に X,REC_NOT_GAP を取得

-- =====================================================
-- Step 2: Session B - id=20 をロック（まだデッドロックなし）
-- 別ターミナル(Session B)で実行
-- =====================================================
-- BEGIN;
-- SELECT * FROM accounts WHERE id = 20 FOR UPDATE;
-- -- id=20 に X,REC_NOT_GAP を取得
-- -- この時点ではデッドロックなし（異なるリソース）

-- =====================================================
-- Step 3: Observer - 現在のロック状態確認
-- 別ターミナル(Observer)で実行
-- =====================================================
-- source sql/helpers/observe_locks.sql
-- -- Session A: id=10 に X,REC_NOT_GAP (GRANTED)
-- -- Session B: id=20 に X,REC_NOT_GAP (GRANTED)

-- =====================================================
-- Step 4: Session A - id=20 をロック試行（Session Bが保持中 → 待機）
-- 別ターミナル(Session A)で実行
-- =====================================================
-- SELECT * FROM accounts WHERE id = 20 FOR UPDATE;
-- -- ← Session Bが id=20 を保持しているため待機

-- =====================================================
-- Step 5: Session B - id=10 をロック試行（デッドロック発生！）
-- 別ターミナル(Session B)で実行
-- =====================================================
-- SELECT * FROM accounts WHERE id = 10 FOR UPDATE;
-- -- ← 循環待機でデッドロック！
-- -- ERROR 1213 (40001): Deadlock found when trying to get lock; try restarting transaction
--
-- InnoDBはどちらかを被害者(victim)として選び自動ロールバック
-- 通常: 修正行数が少ない方（ここでは同じなので後からデッドロックを引き起こした方）

-- =====================================================
-- Step 6: Observer - デッドロック後の状態確認
-- 別ターミナル(Observer)で実行
-- =====================================================
-- source sql/helpers/show_engine_status.sql
-- -- 出力の "LATEST DETECTED DEADLOCK" セクションを確認:
-- --   - どのトランザクションがロールバックされたか
-- --   - 各トランザクションが保持・待機していたロック
-- --   - デッドロック検知のタイムスタンプ
--
-- source sql/helpers/observe_trx.sql
-- -- ロールバックされたトランザクションは消えている

-- =====================================================
-- Step 7: 生き残ったセッションのクリーンアップ
-- =====================================================
-- デッドロックでロールバックされた側（例: Session B）:
-- -- ERROR 1213 を受け取った後、トランザクションは自動ロールバック済み
-- -- ROLLBACK; は不要だが実行しても問題なし
--
-- 生き残った側（例: Session A）:
-- -- id=20のロックが解放され、FOR UPDATEが完了する
-- COMMIT;  -- または ROLLBACK;

-- =====================================================
